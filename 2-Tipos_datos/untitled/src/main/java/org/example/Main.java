package org.example;

public class Main{
    public static void main(String[]args){
        //tipos numericos enteros
        byte byteejemplo = 100;
        short shortejemplo = 3000;
        int intejemplo = 2000000;
        long longejemplo = 20000000000000l;

        //tipos numericos con decimales
        float floatejemplo = 10.0f;
        double doubleejemplo = 20.00000;

        //tipo booleano
        boolean booleanoejemplo = true;

        //tipos caracter
        char charejemplo = 'A';

        //tipo cadena de texto
        String stringejemplo = "hola";

        // Mostrando los valores en consola
        System.out.println("Tipo byte: " + byteejemplo);
        System.out.println("Tipo short: " + shortejemplo);
        System.out.println("Tipo int: " + intejemplo);
        System.out.println("Tipo long: " + longejemplo);
        System.out.println("Tipo float: " + floatejemplo);
        System.out.println("Tipo double: " + doubleejemplo);
        System.out.println("Tipo boolean: " + booleanoejemplo);
        System.out.println("Tipo char: " + charejemplo);
        System.out.println("Tipo String: " + stringejemplo);

    }
}